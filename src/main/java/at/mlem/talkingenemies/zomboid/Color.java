package at.mlem.talkingenemies.zomboid;

public class Color {
    private final float r;
    private final float g;
    private final float b;

    public Color(float r, float g, float b) {

        this.r = r;
        this.g = g;
        this.b = b;
    }

    public float r() {
        return r;
    }
    public float g() {
        return g;
    }
    public float b() {
        return b;
    }

}
